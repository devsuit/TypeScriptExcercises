function city_country(city, country) {
    return "".concat(city, ", ").concat(country);
}
var city1 = city_country("Lahore", "Pakistan");
var city2 = city_country("Los Angeles", "USA");
var city3 = city_country("Birmingham", "United Kingdom");
console.log(city1);
console.log(city2);
console.log(city3);
